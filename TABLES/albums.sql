/*Делаем запрос где, жанром которых является рок, а продажи были меньше, чем 25 миллионов копий*/
PROMPT Все альбомы где, жанром которых является рок, а продажи были меньше, чем 25 миллионов копий 
SELECT
    *
FROM
    albums
WHERE
        genre = 'Rock' 
    AND sales_in_millions <= 25
ORDER BY
    released;
/*Делаем запрос для вывода данных об альбомах в жанре Hard rock или Folk rock, отсортированые по жанру*/
PROMPT Все альбомы в жанре Hard rock или Folk rock, отсортированые по жанру
SELECT
    *
FROM
    albums
WHERE
    genre IN ( 'Hard rock', 'Folk rock' )
ORDER BY
    genre;
